DROP TABLE IF EXISTS users, requests, items, bookings, comments CASCADE;

CREATE TABLE users (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(255) NOT NULL,
email VARCHAR(512) NOT NULL,
CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE requests (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
description VARCHAR(512) NOT NULL,
requestor_id BIGINT NOT NULL,
CONSTRAINT fk_request_from_users FOREIGN KEY (requestor_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE items (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(255) NOT NULL,
description VARCHAR(512) NOT NULL,
is_available BOOLEAN NOT NULL,
owner_id BIGINT NOT NULL,
request_id BIGINT NULL,
CONSTRAINT fk_item_of_user FOREIGN KEY (owner_id) REFERENCES users (id) ON DELETE CASCADE,
CONSTRAINT fk_item_by_request FOREIGN KEY (request_id) REFERENCES requests (id)
);

CREATE TABLE bookings (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
start_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
end_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
item_id BIGINT NOT NULL,
booker_id BIGINT NOT NULL,
status VARCHAR(8) NOT NULL,
CONSTRAINT fk_item_to_book FOREIGN KEY (item_id) REFERENCES items (id) ON DELETE CASCADE,
CONSTRAINT fk_user_to_book FOREIGN KEY (booker_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE comments (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
text VARCHAR(1024) NOT NULL,
item_id BIGINT NOT NULL,
author_id BIGINT NOT NULL,
created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
CONSTRAINT fk_comments_to_item FOREIGN KEY (item_id) REFERENCES items (id) ON DELETE CASCADE,
CONSTRAINT fk_comments_by_user FOREIGN KEY (author_id) REFERENCES users (id) ON DELETE CASCADE
);